package per.cyj.tutorial.day02;

/**
 * 常见命名规则案例
 *
 * @author chenyongjun
 * @since 2020-02-05
 */
public class Day02Demo03 {

    public static void main(String[] args) {
        /*
         * 常见命名规则
         * 1、见名知意，举例：定义一个学生类class Student{}
         * 2、包：其实就是文件夹，用于把相同的类名进行区分，包名全部小写
         *      a.单级包：people
         *      b.多级包：cn.people
         * 3、类或者接口：
         *      a.一个单词：单词的首字母必须大写，如：Student、Dog
         *      b.多个单词：每个单词的首字母必须大写，如：HelloWord、GoodMorning
         * 4、方法或者变量：
         *      a.一个单词：单词的首字母小写，如：man、age
         *      b.多个单词：从第二个单词开始，每个单词的首字母大写，如：studentAge、showAllNames()
         * 5、常量：
         *      a.一个单词：全部大写，如：PI
         *      b.多个单词，每个字母都大写，用_隔开，如：STUDENT_MAX_AGE
         */
        System.out.println("Hello, World!");
    }
}
