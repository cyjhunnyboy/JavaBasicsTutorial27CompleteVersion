// 这是我的注释案例
public class Day02Demo04 {

    /*
        main方法是主方法，是程序的入口，被JVM调用
     */
    public static void main(String[] args) {
        /*
            注释：用于解释说明程序的文字
            Java中注释的分类格式
            a.单行注释：//
            b.多行注释：/星 星/，注意：多行不可以嵌套使用，而单行是可以的
            c.文档注释：被javadoc工具解析生成一个说明书，面向对象部分讲解，格式：/星星 星/
         */
        System.out.println("好好学习，天天向上。");
    }
}
