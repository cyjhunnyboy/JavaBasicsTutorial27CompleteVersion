package per.cyj.tutorial.day04;

/**
 * for循环语句
 *
 * @author chenyongjun
 * @since 2020-02-07
 */
public class Day04Demo10 {

    public static void main(String[] args) {
        /*
            需求：请在控制台输出数据1-10
         */
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("-------------");

        for (int y = 0; y < 10; y++) {
            System.out.println(y + 1);
        }
    }
}
