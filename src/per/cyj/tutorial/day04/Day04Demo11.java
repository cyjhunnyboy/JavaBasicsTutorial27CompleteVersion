/**
 * for循环语句
 *
 * @author chenyongjun
 * @since 2020-02-07
 */
public class Day04Demo11 {

    public static void main(String[] args) {
        /*
            需求：请在控制台输出数据10-1
         */
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}

