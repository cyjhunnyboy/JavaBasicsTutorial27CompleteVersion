package per.cyj.tutorial.day04;

/**
 * while循环语句
 *
 * @author chenyongjun
 * @since 2020-02-07
 */
public class Day04Demo23 {

    /*
        练习：我国最高山峰是珠穆朗玛峰：8848m，我现在有一张足够大的纸张，厚度为：0.01m。
             请问，我折叠多少次，就可以保证厚度不低于珠穆朗玛峰的高度？

        分析：
            1、定义一个统计变量，默认值是0
            2、最高山峰是珠穆朗玛峰：8848m，这是最终的厚度
            3、一张足够大的纸张，厚度为：0.01m，这是初始厚度
            4、我折叠多少次，就可以保证厚度不低于珠穆朗玛峰的高度？ 折叠一次有什么变化呢？就是厚度是以前的2倍
            5、只要每次变化的厚度没有超过珠穆朗玛峰的高度，就折叠，统计变量++
            6、输出统计变量
     */
    public static void main(String[] args) {
        // 定义一个统计变量，默认值是0
        int count = 0;

        // 最高山峰是珠穆朗玛峰：8848m，这是最终的厚度
        // 一张足够大的纸张，厚度为：0.01m，这是初始厚度
        // 为了简单，我把0.01变成1，同理8848就变成了884800
        int end = 884800;
        int start = 1;
        while (start < end) {
            // 只要每次变化的厚度没有超过珠穆朗玛峰的高度，就折叠，统计变量++
            count++;

            // 折叠一次有什么变化呢？就是厚度是以前的2倍
            start *= 2;

            System.out.println("第" + count + "次厚度是" + start);
        }

        // 输出统计变量
        System.out.println("要叠" + count + "次");
    }
}
